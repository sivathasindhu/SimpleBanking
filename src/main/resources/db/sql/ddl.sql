CREATE SEQUENCE seq_account_no START WITH 100000;

CREATE TABLE CUSTOMER ( ACCOUNT_NO BIGINT GENERATED BY DEFAULT AS SEQUENCE seq_account_no PRIMARY KEY, 
	USER_ID VARCHAR(50), PASSWORD VARCHAR(50), NAME VARCHAR(50), EMAIL_ID VARCHAR(50),BALANCE DOUBLE, 
	UNIQUE (USER_ID, EMAIL_ID));

CREATE TABLE TRANSACTION ( ACCOUNT_NO BIGINT ,TRANSACTION_TYPE VARCHAR(500),TRANSACTION_AMOUNT DOUBLE ,BALANCE DOUBLE, TRANS_DATE DATE );


